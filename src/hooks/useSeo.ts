export function useSeo({ page }: SeoConfig) {
  const { city } = useCity();

  useEffect(() => {
    const data = SEO_DATA[city.id]?.[page] ?? SEO_DATA.moscow[page];

    document.title = data.title;

    let descTag = document.querySelector('meta[name="description"]') as HTMLMetaElement | null;
    if (!descTag) {
      descTag = document.createElement("meta");
      descTag.setAttribute("name", "description");
      document.head.appendChild(descTag);
    }
    descTag.setAttribute("content", data.description);

    let kwTag = document.querySelector('meta[name="keywords"]') as HTMLMetaElement | null;
    if (!kwTag) {
      kwTag = document.createElement("meta");
      kwTag.setAttribute("name", "keywords");
      document.head.appendChild(kwTag);
    }
    kwTag.setAttribute("content", data.keywords);

    const ogTitle = document.querySelector('meta[property="og:title"]') as HTMLMetaElement | null;
    if (ogTitle) ogTitle.setAttribute("content", data.title);

    const ogDesc = document.querySelector('meta[property="og:description"]') as HTMLMetaElement | null;
    if (ogDesc) ogDesc.setAttribute("content", data.description);
  }, [city.id, page]);
}
